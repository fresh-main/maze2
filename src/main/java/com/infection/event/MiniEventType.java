package com.infection.event;

/** Каталог инвентиков. Расширяется по мере добавления новых событий. */
public enum MiniEventType {

    /** Чёрная фигура мгновенно поворачивается к цели, мерцание + звук, рассеивается дымом. */
    JUMPSCARE("Jumpscare", "Чёрная фигура, мгновенное мигание + звук, рассеивается дымом", true),

    /** Принудительный приступ галлюцинаций у выбранных игроков. Без чёрного силуэта. */
    HALLUCINATION_SURGE("Приступ", "Принудительный приступ галлюцинаций у выбранных игроков", false),

    /** Админ телепортируется за спину цели и нависает 3 сек. Виден ТОЛЬКО жертве. */
    LOOMING("Гриф нависает", "Силуэт за спиной жертвы + приступ с фразами «обернись»", true),

    /** Админ мерцает в случайных точках вокруг цели за 4 сек. Виден ТОЛЬКО жертве. */
    FLICKER_PRESENCE("Мигающая фигура", "Силуэт телепортируется в случайных точках вокруг жертвы", true),

    /**
     * Сценарный «бросок чёрной фигуры»: жертва замирает, админ телепортируется ей за спину,
     * жертва плавно поворачивается к нему, админ слегка отступает и затем влетает в неё.
     * Виден ТОЛЬКО жертве. Полностью автоматический, ~4 секунды.
     */
    BLACK_RUSH("Бросок", "Жертва замирает, силуэт за спиной разворачивает её и кидается", true),

    /**
     * Дымовое исчезновение/появление — модель админа становится чёрным силуэтом, видна
     * всем игрокам уровня. Управляется двумя хоткеями: «исчезнуть в дыму» (R, старт)
     * и «появиться из дыма» (T, стоп). Длится бесконечно, пока админ сам не нажмёт
     * «появиться». Без таргета.
     */
    SMOKE("Дым", "Чёрный силуэт админа в облаке дыма (R — скрыться, T — выйти)", true);

    public final String displayName;
    public final String description;
    /** Нужно ли натягивать чёрный силуэт на админа на фазах PREPARING/ACTIVE. */
    public final boolean hasSilhouette;

    MiniEventType(String displayName, String description, boolean hasSilhouette) {
        this.displayName = displayName;
        this.description = description;
        this.hasSilhouette = hasSilhouette;
    }

    public static MiniEventType byOrdinal(int o) {
        MiniEventType[] all = values();
        if (o < 0 || o >= all.length) return JUMPSCARE;
        return all[o];
    }
}
