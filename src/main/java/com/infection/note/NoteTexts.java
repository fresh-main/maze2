package com.infection.note;

import com.infection.capability.InfectionStage;

import java.util.UUID;

/**
 * Шаблоны записки-самочувствия. Привязаны к шагам по 5% заражения (всего 21 уровень
 * от 0 до 100). Получается мелкий шаг — игрок видит, как состояние ползёт.
 *
 * Тексты — дневник бегущего, без процентов и цифр. Игрок не должен знать точный уровень.
 */
public final class NoteTexts {

    private NoteTexts() {}

    /** 21 фраза: индекс i соответствует уровню i*5 ∈ {0, 5, 10, ..., 100}. */
    private static final String[] LEVEL_TEMPLATES = {
            // 0%
            "Самочувствие хорошее.\nБольше нечего сказать...",
            // 5%
            "Лёгкое головокружение. Списываю на жару.",
            // 10%
            "Висок ноет. Забыл пару слов за обедом — не страшно.",
            // 15%
            "Кашель не проходит. Пыль в горле скрипит.",
            // 20%
            "На запястье вылезла тёмная вена. Раньше её не было.",
            // 25%
            "Кожа серее обычного. По ночам слышу шаги в стене.",
            // 30%
            "Шатает. Два раза падал сегодня на ровном месте.",
            // 35%
            "Руки трясутся над ложкой. Еда отдаёт металлом.",
            // 40%
            "В глазах двоится по утрам. Синяки, которых я не помню.",
            // 45%
            "Алби сказал, что я бледный. Я не ответил. Забыл слова.",
            // 50%
            "Вытащили из сада. Я стоял и не помнил, что там делал.",
            // 55%
            "Больно дышать. Тёмные пятна на руках уже не сводятся.",
            // 60%
            "Бен говорит, мои глаза изменились. В зеркало не смотрю.",
            // 65%
            "Меня выгнали из строя. Говорят, я пугаю остальных.",
            // 70%
            "Не помню, ел я сегодня или нет. Руки чужие.",
            // 75%
            "Стены шепчут. Я не разбираю слов. Может, это уже не я пишу.",
            // 80%
            "Вижу людей, которых здесь нет. Они смотрят и молчат.",
            // 85%
            "Когда моргаю — они ближе. Кто-то снаружи запер дверь. Или нет.",
            // 90%
            "Это уже не я пишу. Я вижу, как кто-то держит мою руку.",
            // 95%
            "не я не я не я не я",
            // 100%
            "..."
    };

    /** Возвращает шаблон, соответствующий ближайшему 5%-бакету уровня. */
    public static String forLevel(int level, UUID playerId) {
        int bucket = bucketOf(level);
        return LEVEL_TEMPLATES[bucket];
    }

    /** Бакет = level / 5, обрезанный до диапазона 0..20. */
    public static int bucketOf(int level) {
        int b = Math.max(0, Math.min(level, 100)) / 5;
        return Math.max(0, Math.min(b, LEVEL_TEMPLATES.length - 1));
    }

    /** Совместимость со старыми вызовами. Берёт нижний порог стадии. */
    @Deprecated
    public static String forStage(InfectionStage stage, UUID playerId) {
        int level = stage == null ? 0 : stage.threshold;
        return forLevel(level, playerId);
    }

    /** Строка-приписка, которую добавляем в записку при использовании бинта/пластыря First Aid. */
    public static String bandageAppendix(UUID playerId) {
        String[] opts = {
                "\n\n---\nПриложил бинт. Кажется, на пару минут стало легче.",
                "\n\n---\nПерекрутил руку бинтом. Дышать чуть проще.",
                "\n\n---\nВытер кровь, замотался. Теперь ждать."
        };
        return opts[Math.floorMod(playerId == null ? 0 : playerId.hashCode(), opts.length)];
    }

    /** Приписка при использовании морфина. */
    public static String morphineAppendix(UUID playerId) {
        String[] opts = {
                "\n\n---\nПринял морфин. Голоса стихли. Пока.",
                "\n\n---\nИгла вошла легко. В голове стало тише.",
                "\n\n---\nМорфин. Я больше не слышу стен. Но это ненадолго."
        };
        return opts[Math.floorMod(playerId == null ? 0 : playerId.hashCode(), opts.length)];
    }
}
